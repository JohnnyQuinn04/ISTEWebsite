<main>
    <h2>What is Contrast?</h2>
    <p>
        Contrast refers to the relationship between design elements whose differences are emphasized 
        when placed side by side. Contrast helps highlight the unique characteristics of each 
        element, and allow them to appear visually distinct. High contrast occurs when elements 
        have unique characteristics including color, size, shape, or texture, while low contrast 
        occurs when elements are visually similar and blend together. Web designers often use 
        contrast to highlight or draw attention to specific elements.
    </p>

    <h2>Why is Contrast Important?</h2>
    <p>
        Contrast plays an integral role in establishing visual hierarchy within a design, emphasizing 
        certain elements over others. Using contrast in an application ensures that key content stands 
        out by differentiating elements from the background or surrounding objects. This technique 
        improves the experience of the user by making the site easy to navigate through and tell which 
        content is most important. Contrast is also key when considering accessibility, as it is helpful 
        for users with visual impairments by making content more distinguishable. Using contrast 
        correctly can also enhance legibility through effective font choice, size, and color.
    </p>
