    <h2>What is Repetition?</h2>
    <p>
        Repetition is the act of repeating something. In the context of web design that something 
        is often design elements like colors, patterns, shapes and even layouts. The main role of 
        repetition is to keep the design consistent, or at least to help when grouping elements. 
        Unlike contrast, repetition relies on things being similar to achieve its goals. This leads 
        to some difficulty in balancing the two as too much repetition will limit contrast while 
        too sporadic use of contrast makes grouping related elements by repetition impossible. 
    </p>

    <h2>Why is Repetition Important?</h2>
    <p>
