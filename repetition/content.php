    <h2>What is Repetition?</h2>
    <p>Repetition involves reusing consistent design elements throughout a layout to create a cohesive and unified appearance. By repeating patterns, colors, fonts, or shapes, designers can establish a visual rhythm, making the design more intuitive and aesthetically pleasing.</p>

    <h2>Why is Repetition Important?</h2>
    <ul>
        <li>Creates a sense of consistency across the design.</li>
        <li>Helps users navigate the site by recognizing recurring patterns.</li>
        <li>Establishes a unified and professional look.</li>
        <li>Improves accessibility by maintaining predictable layouts.</li>
    </ul>

    <h2>Examples of Repetition</h2>

    <h3>Example 1: Branding</h3>
    <p>Using the same color scheme, fonts, and logo across all pages of a site reinforces brand identity. This consistency builds trust and familiarity with the user.</p>
    <img src="assets/images/repetition-example1.png" alt="Repetition example: consistent branding elements">

    <h3>Example 2: Buttons</h3>
    <p>Designing buttons with a uniform style throughout the site ensures users recognize interactive elements easily. Repeated button designs improve usability and reduce cognitive load.</p>
